#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	counter;

	counter = 0;
	if (!lst)
		return (0);
	while (lst != NULL)
	{
		counter++;
		lst = lst->next;
	}
	return (counter);
}
